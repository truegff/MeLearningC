/*
	This program demonstrates variable declaration,
	value assignment and finally printing the value to console.
*/
#include <stdio.h>

int main(void) {
	/* int variable declaration */
	int num;

	/* assigning a constant value to a variable */
	num = 100;

	/* printing out the value to console */
	printf("The value is %d\n", num);

	return 0;
}
