/*
	This program demonstrates that printf() function allows
	output of values in the beginning/middle of the screen.
*/
#include <stdio.h>

int main(void) {
	/* variable declaration */
	int num;

	/* variable value assignment */
	num = 1000;

	/* outputting value */
	printf("%d is the value of num\n", num);

	return 0;
}
